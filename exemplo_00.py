def soma(valor_1_para_somar: float, valor_2_para_somar: float) -> float:
    """Função de Soma

    Args:
        valor_1_para_somar (float): valor simples do tipo float
        valor_2_para_somar (float): valor simples do tipo float

    Returns:
        float: retorna a soma de dois valores float
    """
    resultado_da_soma = valor_1_para_somar + valor_2_para_somar
    return resultado_da_soma

print(soma(4, 6))
